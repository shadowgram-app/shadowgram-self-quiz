// 코드 구조: PREFIX-XXXXXXXX (예: SG-A3F9K2M1)
// 타입별 prefix: I=개인, C=커플, G=그룹
window.SG_CODES = window.SG_CODES || {};

function generateCode(type='I', prefix='SG') {
  const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  let code = '';
  for (let i = 0; i < 8; i++) code += chars[Math.floor(Math.random() * chars.length)];
  return `${prefix}${type}-${code}`;
}

function validateCode(code) {
  // localStorage 기반 검증 (베타)
  const stored = JSON.parse(localStorage.getItem('sg_issued_codes') || '{}');
  if (!stored[code]) return { valid: false, reason: '등록되지 않은 코드입니다.' };
  if (stored[code].used) return { valid: false, reason: '이미 사용된 코드입니다.' };
  if (stored[code].expires && new Date(stored[code].expires) < new Date()) {
    return { valid: false, reason: '만료된 코드입니다.' };
  }
  return { valid: true, type: stored[code].type, meta: stored[code] };
}

function markCodeUsed(code, userInfo) {
  const stored = JSON.parse(localStorage.getItem('sg_issued_codes') || '{}');
  if (stored[code]) {
    stored[code].used = true;
    stored[code].usedAt = new Date().toISOString();
    stored[code].usedBy = userInfo;
    localStorage.setItem('sg_issued_codes', JSON.stringify(stored));
  }
}

function issueCode(type='I', label='', expiryDays=null) {
  const code = generateCode(type);
  const stored = JSON.parse(localStorage.getItem('sg_issued_codes') || '{}');
  stored[code] = {
    type,
    label,
    issued: new Date().toISOString(),
    expires: expiryDays ? new Date(Date.now() + expiryDays * 86400000).toISOString() : null,
    used: false,
    couplePartner: null,
  };
  localStorage.setItem('sg_issued_codes', JSON.stringify(stored));
  return code;
}
